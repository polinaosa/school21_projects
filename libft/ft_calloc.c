#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*s;

	if ((s = malloc(nmemb * size)) == NULL)
		return (NULL);
	ft_bzero(s, nmemb * size);
	return (s);
}
